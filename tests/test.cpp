void test_key_expansion() {
    std::array<uint8_t,16> key = {
        0x2b,0x7e,0x15,0x16,0x28,0xae,0xd2,0xa6,
        0xab,0xf7,0x15,0x88,0x09,0xcf,0x4f,0x3c
    };

    auto round_keys = key_expansion(key);

    // round_keys[0] should equal the original key, column-wise
    State expected_rk0 = {{
        {{0x2b,0x28,0xab,0x09}},
        {{0x7e,0xae,0xf7,0xcf}},
        {{0x15,0xd2,0x15,0x4f}},
        {{0x16,0xa6,0x88,0x3c}}
    }};
    assert(round_keys[0] == expected_rk0);

    // round_keys[1] from FIPS 197 example
    State expected_rk1 = {{
        {{0xa0,0x88,0x23,0x2a}},
        {{0xfa,0x54,0xa3,0x6c}},
        {{0xfe,0x2c,0x39,0x76}},
        {{0x17,0xb1,0x39,0x05}}
    }};
    assert(round_keys[1] == expected_rk1);
}

void test_state_conversion() {
    std::array<uint8_t,16> in = {
        0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
        0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f
    };
    State s = bytes_to_state(in);
    auto out = state_to_bytes(s);
    assert(in == out);

    // verify actual column-wise layout
    assert(s[0][0] == 0x00);
    assert(s[1][0] == 0x01);
    assert(s[2][0] == 0x02);
    assert(s[3][0] == 0x03);
    assert(s[0][1] == 0x04);
}

